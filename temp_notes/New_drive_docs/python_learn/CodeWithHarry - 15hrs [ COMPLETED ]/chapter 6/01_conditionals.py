a = 45

# if-elif-else ladder 
if(a < 3):
    print("a is less than 3")
elif(a > 7):
    print("a is greater than 7")
else:
    print("value of a is neither greater than 3 nor 7")

print("Done")


# Multiple if ladder 
if(a < 3):
    print("a is less than 3")
if(a > 7):
    print("a is greater than 7")
if(a > 15):
    print("value of a is greater than 15")

print("Done")